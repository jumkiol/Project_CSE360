public class SetBoundaries {

    private int Low;
    private int High;

    SetBoundaries(int low, int high) {
        Low = low;
        High = high;
    }

    public int getLow() {
        return Low;
    }

    public void setLow(int low) {
        Low = low;
    }

    public int getHigh() {
        return High;
    }

    public void setHigh(int high) {
        High = high;
    }
}
